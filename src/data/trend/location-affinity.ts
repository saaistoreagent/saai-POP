export function getLocationAffinity(location: LocationType | LocationType[], category: Category): number {
  if (Array.isArray(location)) {
    if (location.length === 0) return 0.5;
    const avg = location.reduce((sum, l) => sum + (AFFINITY[l]?.[category] ?? 0.5), 0) / location.length;
    return avg;
  }
  return AFFINITY[location]?.[category] ?? 0.5;
}
